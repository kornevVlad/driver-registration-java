CREATE TABLE IF NOT EXISTS users (
    user_id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    user_name VARCHAR(100) NOT NULL,
    user_last_name VARCHAR(100) NOT NULL,
    type_access VARCHAR(50),
    CONSTRAINT PK_USER PRIMARY KEY (user_id)
);

CREATE TABLE IF NOT EXISTS cars (
    car_id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    car_brand VARCHAR(100) NOT NULL,
    car_model VARCHAR(100) NOT NULL,
    number_car VARCHAR(50) NOT NULL,
    vin_car VARCHAR(100) NOT NULL UNIQUE,
    year_release_car INTEGER NOT NULL,
    type_car VARCHAR(50),
    type_rent_driver VARCHAR(50),
    CONSTRAINT PK_NUMBER_CAR PRIMARY KEY (car_id)
);

CREATE TABLE IF NOT EXISTS drivers (
    driver_id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    driver_name VARCHAR(100) NOT NULL,
    driver_last_name VARCHAR(100) NOT NULL,
    number_driver_license VARCHAR(50) NOT NULL UNIQUE,
    driving_experience INTEGER NOT NULL,
    type_rent_car VARCHAR(50),
    CONSTRAINT PK_NUMBER_DRIVER PRIMARY KEY (driver_id)
);

CREATE TABLE IF NOT EXISTS rents (
    rent_id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    driver_id BIGINT NOT NULL,
    car_id BIGINT NOT NULL,
    CONSTRAINT PK_RENT_DRIVER_FOR_CAR PRIMARY KEY (rent_id),
    CONSTRAINT FK_RENT_DRIVER_FOR_DRIVER FOREIGN KEY (driver_id)
        REFERENCES drivers (driver_id),
    CONSTRAINT FK_RENT_CAR_FOR_CAR FOREIGN KEY (car_id)
        REFERENCES cars (car_id)
);